describe('reverseString', function() {
  it('reverses a string', function() {
    expect(reverseString('never')).to.equal('reven');
  });
});

describe('reverseString', function(){
	it('reverses a string', function(){
		expect(reverseString('cat').to.equal('tac'))
	});
});

